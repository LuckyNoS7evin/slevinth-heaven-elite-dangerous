    /// <summary>
    /// Gets the base value for a species variant. Returns 0 if not found.
    /// </summary>
    /// <param name="variantName">The localized variant name</param>
    /// <returns>The base credit value, or 0 if not found</returns>
    public static long GetValue(string variantName)
    {
        return SpeciesValues.TryGetValue(variantName, out long value) ? value : 0;
    }

    /// <summary>
    /// Calculates the first discovery bonus (Value * 4)
    /// The bonus is separate from the base value.
    /// Total payout = Value + Bonus = Value + (Value * 4) = Value * 5
    /// </summary>
    /// <param name="baseValue">The base value of the species</param>
    /// <returns>The first discovery bonus value (4x the base value)</returns>
    public static long CalculateFirstDiscoveryBonus(long baseValue)
    {
        return baseValue * 4;
    }
